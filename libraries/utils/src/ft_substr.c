#include "utils.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*substring;
	size_t	s_len;

	if (!s)
		return (0);
	s_len = ft_strlen(s);
	if (start > s_len)
		return (ft_strdup(""));
	else if (start + len > s_len)
		len = s_len - start;
	substring = ft_calloc(sizeof(char), (len + 1));
	if (!substring)
		return (0);
	ft_strlcpy(substring, &s[start], len + 1);
	return (substring);
}
